CREATE TABLE EMPLOYEE (
    EMP_ID INTEGER NOT NULL,
    EMP_LNAME VARCHAR2(50) NOT NULL,
    EMP_FNAME VARCHAR2(50) NOT NULL,
    OFFICE_NUM INTEGER,
    FLOOR_NUM INTEGER,
    EMP_PHONE INTEGER NOT NULL,
    EMP_EMAIL VARCHAR2(50),
    EMP_ADDRESS VARCHAR2(255),
    PRIMARY KEY (EMP_ID)
);

CREATE TABLE MEETING (
    MEET_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    ROOM_NUM INTEGER NOT NULL,
    FLOOR_NUM INTEGER NOT NULL,
    START_TIME INTEGER NOT NULL,
    MEET_DATE DATE NOT NULL,
    PRIMARY KEY (MEET_ID, EMP_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE ALERTNOTIFICATION (
    NOTIFICATION_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    NOTIFICATION_DATE DATE NOT NULL,
    NOTIFICATION_TYPE VARCHAR2(20) NOT NULL,
    PRIMARY KEY (NOTIFICATION_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)    
);

CREATE TABLE SYMPTOM (
    ROW_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    REPORT_DATE DATE NOT NULL,
    SYMPTOM_ID INTEGER NOT NULL,
    PRIMARY KEY (ROW_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE COVID19SCAN (
    SCAN_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    SCAN_DATE DATE NOT NULL,
    SCAN_TIME INTEGER NOT NULL,
    TEMPERATURE INTEGER NOT NULL,
    PRIMARY KEY (SCAN_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE COVID19TEST (
    TEST_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    TEST_LOCATION VARCHAR2(50) NOT NULL,
    TEST_DATE DATE NOT NULL,
    TEST_TIME INTEGER NOT NULL,
    TEST_RESULT VARCHAR2(20) NOT NULL,
    PRIMARY KEY (TEST_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE COVID19CASE (
    CASE_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    CASE_DATE DATE NOT NULL,
    RESOLUTION Varchar2(50) not null,
    PRIMARY KEY (CASE_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);

CREATE TABLE HEALTHSTATUS (
    ROW_ID INTEGER NOT NULL,
    EMP_ID INTEGER NOT NULL,
    STATUS_DATE DATE NOT NULL,
    EMP_STATUS Varchar2(50) NOT NULL,
    PRIMARY KEY(ROW_ID),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID)
);


